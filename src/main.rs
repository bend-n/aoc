pub unsafe fn p2(x: &'static [u8]) -> impl Debug {
    let x = x
        .行()
        .map(|x| {
            let (expected, buttons) = x.μ(' ');
            let mut c = vec![];
            let mut c_ = 0;
            let mut r = vec![];
            let (mut buttons, power) = buttons.rsplit_once(|x| *x == b' ').unwrap();

            while let Some(x) = buttons.get(0) {
                match x {
                    b'(' => {}
                    d @ b'0'..=b'9' => {
                        c_ *= 10;
                        c_ += (d - b'0') as u32
                    }
                    b',' => {
                        c.push(c_);
                        c_ = 0;
                    }
                    b')' => {
                        c.push(c_);
                        c_ = 0;

                        r.push(c.clone());
                        c.clear();
                    }
                    b' ' => {}
                    x => panic!("{}", *x as char),
                }
                buttons = &buttons[1..];
            }
            let mut power = &power[1..];
            let mut pvec = vec![];
            let mut c = 0;
            while let Some(x) = power.get(0) {
                match x {
                    d @ b'0'..=b'9' => {
                        c *= 10;
                        c += (d - b'0') as u32
                    }
                    b',' => {
                        pvec.push(c);
                        c = 0;
                    }
                    b'}' => {
                        pvec.push(c);
                        break;
                    }
                    b' ' => {}
                    x => panic!("{}", *x as char),
                }
                power = &power[1..];
            }

            (
                expected[1..expected.len() - 1]
                    .iter()
                    .map(|x| *x == b'#')
                    .collect::<Vec<_>>(),
                r,
                pvec,
            )
        })
        .collect::<Vec<_>>();
    let mut sum = 0;
    z3::with_z3_config(&z3::Config::default(), || {
        for (xpected, buttons, power) in x {
            let o = z3::Optimize::new();
            let p = (0..buttons.len())
                .map(|_| Int::fresh_const("p"))
                .inspect(|x| o.assert(&x.ge(0)))
                .collect::<Vec<_>>();
            for j in 0..power.len() {
                let sat = &buttons
                    .iter()
                    .enumerate()
                    .filter(|(_, b)| b.contains(&(j as u32)))
                    .map(|(x, _)| p[x].clone())
                    .reduce(|a, b| a + b)
                    .unwrap()
                    .eq(power[j]);
                o.assert(sat);
            }
            let s = p.iter().cloned().reduce(|a, b| a + b).unwrap();
            o.minimize(&s);

            assert_eq!(o.check(&[]), z3::SatResult::Sat);
            let m = o.get_model().unwrap();
            sum += m.eval(&s, true).unwrap().as_i64().unwrap();
        }
    });
    sum
}

